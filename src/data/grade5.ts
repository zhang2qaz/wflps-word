import type { Word, Poem, Sentence, CharInfo, CharKind } from './vocabulary';

type Deep = {
  split?: string;
  kind?: CharKind;
  hook?: string;
  family?: string;
  warn?: string;
};

// 五元组:基础字数据;六元组:再带一份深度元数据。
type Row =
  | [c: string, pinyin: string, meaning: string, radical: string, strokes: number]
  | [c: string, pinyin: string, meaning: string, radical: string, strokes: number, deep: Deep];

type Lesson = [lesson: string, chars: Row[]];

function build(
  semester: '上' | '下',
  unit: number,
  unitTitle: string,
  lessons: Lesson[],
  isDeep = false,
): Word[] {
  const out: Word[] = [];
  const prefix = `g5${semester === '上' ? 'a' : 'b'}`;
  let n = 0;
  for (const [lesson, chars] of lessons) {
    for (const row of chars) {
      n += 1;
      const [c, pinyin, meaning, radical, strokes, deep] = row as
        [string, string, string, string, number, Deep | undefined];
      const ci: CharInfo = { c, pinyin, radical, strokes, ...(deep ?? {}) };
      out.push({
        id: `${prefix}${unit}-${String(n).padStart(2, '0')}`,
        char: c, pinyin, meaning,
        grade: 5, semester, unit, unitTitle, lesson,
        type: 'word',
        examples: [],
        sentence: '',
        tip: isDeep
          ? '统编版五年级·会写字。把字拆成熟悉的部件,再用字族联想记住。'
          : '统编版五年级·会写字。先看部首/笔画记字形,再读释义记用法。',
        chars: [ci],
        draft: false,
      });
    }
  }
  return out;
}

